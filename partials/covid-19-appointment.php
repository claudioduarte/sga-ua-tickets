  <h2 class="title" style="background-color: #cc0000;">
    <span>AGENDAMENTO DE ATENDIMENTO PRESENCIAL - COVID-19</span>
  </h2>
  <div class="content">
    <p>
      O pedido de agendamento para atendimento presencial nos Serviços de Gestão Académica (SGA) deve ser efetuado através da plataforma: <a href="https://cms.ua.pt/sga/event-created">https://cms.ua.pt/sga/event-created</a>.
    </p>
    <p>
      Pode escolher, de entre os horários disponíveis, aquele que melhor se adequa à sua disponibilidade. Após preenchimento da informação solicitada e submissão do pedido, receberá por email a confirmação do agendamento efetuado (dia e hora).
    </p>
    <p>
      Considerando as medidas preventivas relativas à situação epidemiológica COVID-19, informamos que os SGA mantêm encerrado o acesso aos postos de atendimento, pelo que deverão dirigir-se ao balcão do Segurança para permissão de entrada. O uso de máscara é obrigatório.
    </p>
